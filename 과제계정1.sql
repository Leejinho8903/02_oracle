SELECT
      STUDENT_NAME "학생 이름"
     ,STUDENT_ADDRESS "주소지"
  FROM TB_STUDENT
 ORDER BY STUDENT_NAME;

SELECT
        STUDENT_NAME
      , STUDENT_SSN
  FROM TB_STUDENT
  WHERE ABSENCE_YN = 'Y'
 ORDER BY STUDENT_SSN DESC;

SELECT 
        STUDENT_NAME 학생이름
      , STUDENT_NO 학번
      , STUDENT_ADDRESS "거주지 주소"
  FROM TB_STUDENT
 WHERE STUDENT_NO LIKE '9%'
    AND (STUDENT_ADDRESS LIKE '경기도%'
    OR STUDENT_ADDRESS LIKE '강원도%');
   
 

SELECT
      P.PROFESSOR_NAME
     ,P.PROFESSOR_SSN
  FROM TB_PROFESSOR P
  JOIN TB_DEPARTMENT D ON (P.DEPARTMENT_NO = D.DEPARTMENT_NO)
  WHERE DEPARTMENT_NAME = '법학과'
 ORDER BY PROFESSOR_SSN;

SELECT
        STUDENT_NO
      , POINT
  FROM TB_GRADE
  WHERE CLASS_NO = 'C3118100'
    AND TERM_NO = '200402'
  ORDER BY POINT DESC;
  
SELECT 
       S.STUDENT_NO
     , S.STUDENT_NAME
     , D.DEPARTMENT_NAME 
  FROM TB_STUDENT S 
  JOIN TB_DEPARTMENT D ON (S.DEPARTMENT_NO = D.DEPARTMENT_NO)
  ORDER BY S.STUDENT_NAME DESC;

SELECT
        CLASS_NAME
      , DEPARTMENT_NAME
  FROM TB_CLASS 
  JOIN TB_DEPARTMENT USING(DEPARTMENT_NO);

SELECT
       C.CLASS_NAME
     , P.PROFESSOR_NAME
  FROM TB_CLASS C
   JOIN TB_CLASS_PROFESSOR PR ON (C.CLASS_NO = PR.CLASS_NO)
   JOIN TB_PROFESSOR P ON(PR.PROFESSOR_NO = P.PROFESSOR_NO)
   WHERE PR.PROFESSOR_NO = P.PROFESSOR_NO;

SELECT 
       C.CLASS_NAME
     , P.PROFESSOR_NAME
  FROM TB_CLASS C
  JOIN TB_CLASS_PROFESSOR PR USING(CLASS_NO)
  JOIN TB_PROFESSOR P ON(PR.PROFESSOR_NO = P.PROFESSOR_NO)
  JOIN TB_DEPARTMENT D ON(D.DEPARTMENT_NO = P.DEPARTMENT_NO)
   AND CATEGORY = '인문사회';

SELECT
        S.STUDENT_NO "학번"
      , S.STUDENT_NAME "학생 이름"
      , ROUND(AVG(POINT),1) "전체 평점"
   FROM TB_STUDENT S
   JOIN TB_GRADE G ON(S.STUDENT_NO = G.STUDENT_NO)
   JOIN TB_DEPARTMENT D ON(D.DEPARTMENT_NO = S.DEPARTMENT_NO) 
WHERE D.DEPARTMENT_NAME = '음악학과'
GROUP BY (S.STUDENT_NO,
           S.STUDENT_NAME) ;


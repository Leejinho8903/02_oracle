SELECT
        DEPARTMENT_NAME 학과이름
       ,STUDENT_NAME 학생이름
       ,PROFESSOR_NAME 지도교수이름
  FROM TB_DEPARTMENT D
  JOIN TB_STUDENT S ON (S.DEPARTMENT_NO = D.DEPARTMENT_NO)
  JOIN TB_PROFESSOR P ON(P.PROFESSOR_NO = S.COACH_PROFESSOR_NO)
  WHERE STUDENT_NO = 'A313047';

SELECT
        STUDENT_NAME
      , TERM_NO "TERM_NAME"
  FROM TB_STUDENT S
  JOIN TB_DEPARTMENT D ON (S.DEPARTMENT_NO = D.DEPARTMENT_NO)
  JOIN TB_GRADE G ON(S.STUDENT_NO = G.STUDENT_NO)
  JOIN TB_CLASS C ON(C.CLASS_NO = G.CLASS_NO)
  WHERE TERM_NO LIKE '2007%'
  AND CLASS_NAME = '인간관계론';

SELECT
       C.CLASS_NAME
     , DEPARTMENT_NAME
  FROM TB_CLASS C
  JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
  LEFT JOIN TB_CLASS_PROFESSOR USING(CLASS_NO)
  WHERE CATEGORY = '예체능'
    AND PROFESSOR_NO IS NULL;

SELECT
      S.STUDENT_NAME 학생이름
     ,NVL(P.PROFESSOR_NAME, '지도교수미지정') "지도교수"
  FROM TB_STUDENT S
  LEFT JOIN TB_PROFESSOR P ON(P.PROFESSOR_NO = S.COACH_PROFESSOR_NO)
  JOIN TB_DEPARTMENT D ON(D.DEPARTMENT_NO = S.DEPARTMENT_NO)
  WHERE DEPARTMENT_NAME = '서반아어학과' 
  ORDER BY STUDENT_NO;
  


      
  
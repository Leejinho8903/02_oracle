SELECT 
       ROUND(AVG(POINT),1) 평점
  FROM TB_STUDENT S
      ,TB_GRADE G
 WHERE G.STUDENT_NO = 'A517178';
--10
SELECT
       DEPARTMENT_NO "학과번호"
     , COUNT(*) "학생수(명)"
  FROM TB_STUDENT
  GROUP BY DEPARTMENT_NO
  ORDER BY 1;
 


SELECT 
       DEPARTMENT_NO 학과코드명
      ,COUNT(DECODE(ABSENCE_YN,'Y' ,1)) "휴학생 수"
    FROM TB_STUDENT
 GROUP BY DEPARTMENT_NO
 ORDER BY 1;
 
       

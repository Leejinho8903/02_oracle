SELECT
       S.STUDENT_NO 학번
     , S.STUDENT_NAME 이름
     , S.ENTRANCE_DATE 입학년도
  FROM TB_STUDENT S      
  WHERE DEPARTMENT_NO = '002'
  ORDER BY S.ENTRANCE_DATE;
